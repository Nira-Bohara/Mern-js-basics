// ================= Variables =================

// JavaScript has three main ways to declare variables: var, let, const

// 1️⃣ var → can be redeclared and reassigned
var a = 30;     // Declare 'a' with initial value 30
a = 'str';      // Reassign 'a' to a string
console.log(a); // Output: 'str'

// 2️⃣ let → can be reassigned but cannot be redeclared in the same scope
let b = 'hello'; // Declare 'b' with initial value 'hello'
b = 'world';     // Reassign 'b' to a new value
console.log(b);  // Output: 'world'

// 3️⃣ const → cannot be redeclared or reassigned
const c = 23;   // Declare constant 'c'
console.log(c); // Output: 23

// ================= Key Notes =================
// - Use 'var' for legacy code, avoid in modern JS projects
// - Use 'let' when the variable value needs to change
// - Use 'const' for values that should never change


